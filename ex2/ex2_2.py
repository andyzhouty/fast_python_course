# 这个程序会介绍Python中基本的变量
a_number = 3 # 这是一个整数
print(type(a_number)) # 会输出 <class 'int'>，代表整数
a_float = 3.14 # 这是一个浮点数（小数）
print(type(a_float))
a_boolean = True # 这是一个布尔类型，代表True(真)或False(假)
print(type(a_boolean)) # 会输出 <class 'bool'>
a_list = list()
print(type(a_list))

print(isinstance(a_number, int))
